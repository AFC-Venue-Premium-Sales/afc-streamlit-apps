import streamlit as st
from msal import ConfidentialClientApplication
from dotenv import load_dotenv
import os
import sales_performance
import user_performance_api

# Load environment variables
load_dotenv()

# Azure AD Configuration
CLIENT_ID = os.getenv("CLIENT_ID")
TENANT_ID = os.getenv("TENANT_ID")
CLIENT_SECRET = os.getenv("CLIENT_SECRET")
REDIRECT_URI = os.getenv("REDIRECT_URI")
AUTHORITY = f"https://login.microsoftonline.com/{TENANT_ID}"
SCOPES = ["User.Read"]

# Debugging: Print environment variables
print(f"TENANT_ID: {TENANT_ID}")
print(f"CLIENT_ID: {CLIENT_ID}")
print(f"AUTHORITY: {AUTHORITY}")
print(f"REDIRECT_URI: {REDIRECT_URI}")

# MSAL Confidential Client Application
try:
    app = ConfidentialClientApplication(
        client_id=CLIENT_ID,
        client_credential=CLIENT_SECRET,
        authority=AUTHORITY
    )
    print("MSAL initialized successfully.")
except Exception as e:
    print(f"Error initializing MSAL: {e}")

# Initialize session states
if "authenticated" not in st.session_state:
    st.session_state["authenticated"] = False
if "access_token" not in st.session_state:
    st.session_state["access_token"] = None

# Azure AD Login URL Generation
def azure_ad_login():
    return app.get_authorization_request_url(scopes=SCOPES, redirect_uri=REDIRECT_URI)

# App Header
st.title("üèüÔ∏è AFC Venue - MBM Hospitality")

if not st.session_state["authenticated"]:
    # Description
    st.markdown("""
    **Welcome to the Venue Hospitality Dashboard!**  
    This app provides insights into MBM Sales Performance and User Metrics.

    **MBM Sales Performance**:  
    Analyse sales from MBM hospitality.

    **Premium Exec Metrics**:  
    View and evaluate performance metrics from the Premium Team.
    """)

    # Step 1: Login Button
    if st.button("üîê Login with SSO"):
        login_url = azure_ad_login()
        st.markdown(f"[Click here to log in]({login_url})")

    # Step 2: Auto-fetch Query Params for Authorization Code
    query_params = st.experimental_get_query_params()
    print(f"Query Params: {query_params}")  # Debugging query parameters
    if "code" in query_params:
        auth_code = query_params["code"]
        print(f"Authorization Code: {auth_code}")  # Debugging authorization code
        try:
            # Exchange code for token
            result = app.acquire_token_by_authorization_code(
                code=auth_code,
                scopes=SCOPES,
                redirect_uri=REDIRECT_URI
            )
            print(f"Token Result: {result}")  # Debugging token result
            if "access_token" in result:
                st.session_state["access_token"] = result["access_token"]
                st.session_state["authenticated"] = True
                st.success("üéâ Login successful!")
                st.rerun()  # Refresh to display authenticated view
            else:
                st.error(f"Error: {result.get('error_description')}")
        except Exception as e:
            st.error(f"An error occurred: {e}")
            print(f"Error acquiring token: {e}")
else:
    # Authenticated User View
    st.sidebar.title("üß≠ Navigation")
    app_choice = st.sidebar.radio("Go to", ["üìä Sales Performance", "üìà User Performance"])

    if app_choice == "üìä Sales Performance":
        st.write("Running Sales Performance Module...")
        sales_performance.run_app()

    elif app_choice == "üìà User Performance":
        st.write("Running User Performance Module...")
        user_performance_api.run_app()

    # Logout Button
    if st.sidebar.button("üîì Logout"):
        st.session_state["authenticated"] = False
        st.session_state["access_token"] = None
        st.rerun()

# Test token acquisition directly
try:
    token_result = app.acquire_token_for_client(scopes=["https://graph.microsoft.com/.default"])
    if "access_token" in token_result:
        print("Direct Token Acquisition Successful")
    else:
        print(f"Error during direct token acquisition: {token_result.get('error_description')}")
except Exception as e:
    print(f"Error during direct token acquisition: {e}")
